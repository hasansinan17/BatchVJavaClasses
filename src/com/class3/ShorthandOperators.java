package com.class3;

public class ShorthandOperators {
	public static void main(String[] args) {
		int num3=1000;
		num3+=1000;// shortcut for printing sysco+ctrl+space
		System.out.println(num3);
		
		int num4=50;
		num4+=20;
		System.out.println(num4);
		
		num4=40;
		num4-=10;
		System.out.println(num4);
		
		num4=6;
		num4%=4;
		System.out.println(num4);
		
		num4=9;
		num4*=3;
		System.out.println(num4); 
		
		num4=60;
		num4/=3;
		System.out.println(num4);
	}
	

}
